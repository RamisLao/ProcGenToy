using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public static class StringFormatting
{
    public static string FirstLetterToUpper(string str)
    {
        if (str == null)
            return null;

        if (str.Length > 1)
            return char.ToUpper(str[0]) + str.Substring(1);

        return str.ToUpper();
    }
}
